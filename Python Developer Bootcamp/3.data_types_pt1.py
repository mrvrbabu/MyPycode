# *_*_*_* -------------------------  Numbers -------------------------------

#Integer
age = 21 
print(age)
print(type(age))

# Float 

my_age = 18.5
grade = 4.9

print("age is", my_age)
print(type(my_age))
print("grade is ", grade)
print(type(grade))

# *_*_*_* -------------------------  Strings -------------------------------

movie_name = "The good, bad and ugly"
print("Movie name is ", movie_name)
print(type(movie_name))

# *_*_*_* -------------------------  Booleans ------------------------------

alarm = True 
status = False 
print(type(alarm))
print(type(status))



